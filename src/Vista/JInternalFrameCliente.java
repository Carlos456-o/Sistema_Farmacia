    private void jBAgregarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarPActionPerformed
        String nomb = jTextNombresC.getText();
String apell = jTextApellidoC.getText();
String ema = jTextEmailC.getText();
int ced = Integer.parseInt(jTextCedulaC.getText());
int tel = Integer.parseInt(jTextTelefonoC.getText());

if (nomb.contentEquals("") || apell.contentEquals("") || 
    ema.contentEquals("") || ced <= 0 ||
    tel <= 0 ) {
    JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios");
    
}else{
    try {
        
    Producto cli = new DAOClientes().Insertar(nomb, apell, ema , ced, tel);
    JOptionPane.showMessageDialog(rootPane, "Registro agregado");
} catch (Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(rootPane, "No se agregó el registro");
}

limpiarCampos();
}
    }//GEN-LAST:event_jBAgregarPActionPerformed

    private void jBEditarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarPActionPerformed
        int fila = this.jTableClientes.getSelectedRow(); // Se obtiene #fila seleccionada
if (fila == -1) {
    JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
} else { // Se toma cada campo de la tabla del registro seleccionado
    try {
        int id = Integer.parseInt((String) this.jTableClientes.getValueAt(fila, 0).toString());
        String nom = (String) this.jTableClientes.getValueAt(fila, 1);
        String apell = (String) this.jTableClientes.getValueAt(fila, 2);
        String ema = (String) this.jTableClientes.getValueAt(fila, 3);
        int ced = Integer.parseInt((String) this.jTableClientes.getValueAt(fila, 4));
        int tel = Integer.parseInt((String) this.jTableClientes.getValueAt(fila, 5));
        
        // Se ubican en las cajas de textos los datos capturados de la tabla
        jTextIdClientes.setText("" + id);
        jTextNombresC.setText(nom);
        jTextApellidoC.setText(apell);
        jTextEmailC.setText(ema);
        jTextCedulaC.setText(String.valueOf(ced));
        jTextTelefonoC.setText(String.valueOf(tel));
    } catch (NumberFormatException e) {
        e.printStackTrace();
    }
}
    }//GEN-LAST:event_jBEditarPActionPerformed

