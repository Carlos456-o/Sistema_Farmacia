    public void obtenerDatos() {
    List<Clientes> clientes = new DAOClientes().ObtenerDatos();
    DefaultTableModel modelo = new DefaultTableModel();
    String[] columnas = {"id_clientes", "NombreC", "ApellidoC", "EmailC", "CédulaC", "TelefonoC"};
    modelo.setColumnIdentifiers(columnas);
    
    for (Clientes cli : clientes) { 
        String[] renglon = {
            Integer.toString(cli.getId_Clientes()),
            cli.getNombreC(),
            cli.getApellidoC(),
            cli.getEmailC(),
            Integer.toString(cli.getCedulaC()),
            Double.toString(cli.getTelefonoC()),
           
        };
        modelo.addRow(renglon);
    }
    jTableClientes.setModel(modelo); 
}
    public void actualizarClientes() {
    int id = Integer.parseInt(this.jTextIdClientes.getText());
    String nom = this.jTextNombresC.getText();
    String apell = this.jTextApellidoC.getText();
    String ema = this.jTextEmailC.getText();
    int ced = Integer.parseInt(this.jTextCedulaC.getText());
    int tel = Integer.parseInt(this.jTextTelefonoC.getText());
    

    DAOClientes dao = new DAOClientes();
    int res = dao.Actualizar(id, nom, apell, ema, ced, tel);
    if (res == 1) {
        JOptionPane.showMessageDialog(rootPane, "Autor Actualizado!");
    } else {
        JOptionPane.showMessageDialog(rootPane, "¡Ocurrió un ERROR!");
    }
}

