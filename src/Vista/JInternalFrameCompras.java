    public void obtenerDatos() {
    List<Compras> compras = new DAOCompras().ObtenerDatos();
    DefaultTableModel modelo = new DefaultTableModel();
    String[] columnas = {"ID_Compras","Fecha_Compras"};
    modelo.setColumnIdentifiers(columnas);
    
    for (Compras com : compras) { 
        String[] renglon = {
            Integer.toString(com.getID_compras()),
            com.getFecha_Compras().toString()
        };
        modelo.addRow(renglon);
    }
    jTableCompras.setModel(modelo); 
}
    public void actualizarProducto() {
    int ID = Integer.parseInt(this.jTextIID_Compras.getText());
    Date fc = Date.valueOf(this.jTextFecha_Compras.getText());


    DAOCompras dao = new DAOCompras();
    int res = dao.Actualizar(ID, fc);
    if (res == 1) {
        JOptionPane.showMessageDialog(rootPane, "Compra Actualizada!");
    } else {
        JOptionPane.showMessageDialog(rootPane, "¡Ocurrió un ERROR!");
    }
}

