    private void jBAgregarCOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarCOActionPerformed

String fc = jTextFecha_Compras.getText();

if (fc.contentEquals("")) {
    JOptionPane.showMessageDialog(rootPane, "El campo de ser rellenado");
}else{
    try {
    Date fch = Date.valueOf(fc);
    Compras pro = new DAOCompras().Insertar(fch);
    JOptionPane.showMessageDialog(rootPane, "Compra Agregada");
} catch (Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(rootPane, "No se agregó la compra");
}

limpiarCampos();
}
    }//GEN-LAST:event_jBAgregarCOActionPerformed

    private void jBEditarCOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarCOActionPerformed
        int fila = this.jTableCompras.getSelectedRow(); // Se obtiene #fila seleccionada
if (fila == -1) {
    JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
} else { // Se toma cada campo de la tabla del registro seleccionado
    try {
        int ID = Integer.parseInt((String) this.jTableCompras.getValueAt(fila, 0).toString());
        Date fc = Date.valueOf((String) this.jTableCompras.getValueAt(fila, 1).toString());

        // Se ubican en las cajas de textos los datos capturados de la tabla
        jTextIID_Compras.setText("" + ID);
        jTextFecha_Compras.setText(String.valueOf(fc));
    } catch (NumberFormatException e) {
        e.printStackTrace();
    }
}
    }//GEN-LAST:event_jBEditarCOActionPerformed

