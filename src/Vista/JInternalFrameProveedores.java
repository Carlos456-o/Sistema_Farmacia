    public void obtenerDatos() {
    List<Proveedores> proveedores = new DAOProveedores().obtenerDatos();
    DefaultTableModel modelo = new DefaultTableModel();
    String[] columnas = {"Id_Proveedores", "NombresP", "telefonoP", "EmailP","DireccionP"};
    modelo.setColumnIdentifiers(columnas);
    
    for (Proveedores Pro : proveedores) { 
        String[] renglon = {
            Integer.toString(Pro.getId_Proveedores()),
            Pro.getNombreP(),
            Pro.getTelefonoP(),
            Pro.getEmaiP(),
        };
        modelo.addRow(renglon);
    }
    jTableProveedores.setModel(modelo); 
}
    public void actualizarProveedores() {
    int Id_Prov = Integer.parseInt(this.jTextIdProveedores.getText());
    String nomP = this.jTextNombreP.getText();
    String telP = this.jTexttelefonoP.getText();
    String emP = this.jTextEmailP.getText();
    String dicP = this.jTextDireccionP.getText();

    DAOProveedores dao = new DAOProveedores();
    int res = dao.Actualizar(Id_Prov, nomP, telP, emP, dicP);
    if (res == 1) {
        JOptionPane.showMessageDialog(rootPane, "proveedor Actualizado!");
    } else {
        JOptionPane.showMessageDialog(rootPane, "¡Ocurrió un ERROR!");
    }
}

