    private void jBEditarProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarProActionPerformed
        int fila = this.jTableProveedores.getSelectedRow(); // Se obtiene #fila seleccionada
if (fila == -1) {
    JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
} else { // Se toma cada campo de la tabla del registro seleccionado
    try {
        int Id_proveedores = Integer.parseInt((String) this.jTableProveedores.getValueAt(fila, 0).toString());
        String nomP = (String) this.jTableProveedores.getValueAt(fila, 1);
        String telP = (String) this.jTableProveedores.getValueAt(fila, 2);
        String emP = (String) this.jTableProveedores.getValueAt(fila, 3);
        String dicP = ((String) this.jTableProveedores.getValueAt(fila, 4).toString());

        // Se ubican en las cajas de textos los datos capturados de la tabla
        jTextIdProveedores.setText("" + Id_proveedores);
        jTextNombreP.setText(nomP);
        jTexttelefonoP.setText(telP);
        jTextEmailP.setText(emP);
        jTextDireccionP.setText(String.valueOf(dicP));
    } catch (NumberFormatException e) {
        e.printStackTrace();
    }
}
    }//GEN-LAST:event_jBEditarProActionPerformed

