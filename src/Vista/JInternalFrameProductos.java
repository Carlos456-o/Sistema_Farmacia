    private void jBAgregarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarPActionPerformed
        String nomb = jTextNombres.getText();
String cate = jTextCategoria.getText();
String des = jTextDescripcion.getText();
int can = Integer.parseInt(jTextCantidad.getText());
double prec = Double.parseDouble(jTextPrecio.getText());
String fecV = jTextFechaVen.getText();

if (nomb.contentEquals("") || cate.contentEquals("") || 
    des.contentEquals("") || can <= 0 ||
    prec <= 0||
    fecV.contentEquals("")) {
    JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios");
}else{
    try {
    Date fech = Date.valueOf(fecV);
    Producto pro = new DAOProducto().Insertar(nomb, cate, des, can, fech, prec);
    JOptionPane.showMessageDialog(rootPane, "Registro agregado");
} catch (Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(rootPane, "No se agregó el registro");
}

limpiarCampos();
}
    }//GEN-LAST:event_jBAgregarPActionPerformed

    private void jBEditarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarPActionPerformed
        int fila = this.jTableProductos.getSelectedRow(); // Se obtiene #fila seleccionada
if (fila == -1) {
    JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
} else { // Se toma cada campo de la tabla del registro seleccionado
    try {
        int id = Integer.parseInt((String) this.jTableProductos.getValueAt(fila, 0).toString());
        String nom = (String) this.jTableProductos.getValueAt(fila, 1);
        String cate = (String) this.jTableProductos.getValueAt(fila, 2);
        String des = (String) this.jTableProductos.getValueAt(fila, 3);
        int can = Integer.parseInt((String) this.jTableProductos.getValueAt(fila, 4));
        double prec = Double.parseDouble((String) this.jTableProductos.getValueAt(fila, 5));
        Date fec = Date.valueOf((String) this.jTableProductos.getValueAt(fila, 6).toString());

        // Se ubican en las cajas de textos los datos capturados de la tabla
        jTextIdProducto.setText("" + id);
        jTextNombres.setText(nom);
        jTextCategoria.setText(cate);
        jTextDescripcion.setText(des);
        jTextCantidad.setText(String.valueOf(can));
        jTextPrecio.setText(String.valueOf(prec));
        jTextFechaVen.setText(String.valueOf(fec));
    } catch (NumberFormatException e) {
        e.printStackTrace();
    }
}
    }//GEN-LAST:event_jBEditarPActionPerformed

