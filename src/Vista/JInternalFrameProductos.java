    public void obtenerDatos() {
    List<Producto> producto = new DAOProducto().ObtenerDatos();
    DefaultTableModel modelo = new DefaultTableModel();
    String[] columnas = {"id_autor", "Nombres", "Apellidos", "Email", "Cédula", "FechaNac"};
    modelo.setColumnIdentifiers(columnas);
    
    for (Producto pro : producto) { 
        String[] renglon = {
            Integer.toString(pro.getId_productos()),
            pro.getNombre(),
            pro.getCategoria(),
            pro.getDescripcion(),
            Integer.toString(pro.getCantidad()),
            Double.toString(pro.getPrec_unit()),
            pro.getFechaVen().toString()
        };
        modelo.addRow(renglon);
    }
    jTableProductos.setModel(modelo); 
}
    public void actualizarProducto() {
    int id = Integer.parseInt(this.jTextIdProducto.getText());
    String nom = this.jTextNombres.getText();
    String cate = this.jTextCategoria.getText();
    String des = this.jTextDescripcion.getText();
    int can = Integer.parseInt(this.jTextCantidad.getText());
    Date fec = Date.valueOf(this.jTextFechaVen.getText());
    double prec = Double.parseDouble(this.jTextPrecio.getText());

    DAOProducto dao = new DAOProducto();
    int res = dao.Actualizar(id, nom, cate, des, can, fec, prec);
    if (res == 1) {
        JOptionPane.showMessageDialog(rootPane, "Autor Actualizado!");
    } else {
        JOptionPane.showMessageDialog(rootPane, "¡Ocurrió un ERROR!");
    }
}

